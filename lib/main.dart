final leftHomeFromEspWifiProvider = StreamProvider<bool>((ref) {
  final c = StreamController<bool>.broadcast();
  bool? prev;
  ref.listen<bool?>(atHomeNowProvider, (_, cur) {
    if (prev == true && cur == false) c.add(true);
    prev = cur;
  });
  ref.onDispose(c.close);
  return c.stream;
});
