/// TIMER PROVIDER
/// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

class TimerState {
  final Duration total, remaining;
  final bool running, isBreak;
  const TimerState({
    required this.total,
    required this.remaining,
    required this.running,
    required this.isBreak,
  });

  String get mmss {
    final m = remaining.inMinutes.remainder(60).toString().padLeft(2, '0');
    final s = remaining.inSeconds.remainder(60).toString().padLeft(2, '0');
    return '$m:$s';
  }
}

class TimerController extends StateNotifier<TimerState> {
  final Ref ref;
  Timer? _ticker;
  Duration _studiedThisSession = Duration.zero;

  TimerController(this.ref)
      : super(
          const TimerState(
            total: Duration.zero,
            remaining: Duration.zero,
            running: false,
            isBreak: false,
          ),
        );

  void _tick() {
    final next = state.remaining - const Duration(seconds: 1);

    if (!state.isBreak) {
      _studiedThisSession += const Duration(seconds: 1);
    }

    if (next <= Duration.zero) {
      _ticker?.cancel();
      _saveStudyTime();

      // Timer finished
      state = TimerState(
        total: state.total,
        remaining: Duration.zero,
        running: false,
        isBreak: state.isBreak,
      );

      // If it was a focus session, auto-start a break
      if (!state.isBreak) {
        int breakMinutes;
        if (state.total.inMinutes <= 25) {
          breakMinutes = 5;
        } else if (state.total.inMinutes <= 45) {
          breakMinutes = 10;
        } else {
          breakMinutes = 15;
        }
        startBreak(Duration(minutes: breakMinutes));
      }
    } else {
      // Timer continues
      state = TimerState(
        total: state.total,
        remaining: next,
        running: true,
        isBreak: state.isBreak,
      );
    }
  }

  void _saveStudyTime() {
    if (_studiedThisSession > Duration.zero) {
      ref.read(studyLogProvider.notifier).addForToday(_studiedThisSession);
      _studiedThisSession = Duration.zero;
    }
  }

  void startFocus(Duration d) {
    _ticker?.cancel();
    _saveStudyTime(); // Save any previous session
    state = TimerState(total: d, remaining: d, running: true, isBreak: false);
    _ticker = Timer.periodic(const Duration(seconds: 1), (_) => _tick());
  }

  void startBreak(Duration d) {
    _ticker?.cancel();
    _saveStudyTime();
    state = TimerState(total: d, remaining: d, running: true, isBreak: true);
    _ticker = Timer.periodic(const Duration(seconds: 1), (_) => _tick());
  }

  void pause() {
    if (!state.running) return;
    _ticker?.cancel();
    _saveStudyTime();
    state = TimerState(
      total: state.total,
      remaining: state.remaining,
      running: false,
      isBreak: state.isBreak,
    );
  }

  void resume() {
    if (state.running || state.remaining == Duration.zero) return;
    _ticker = Timer.periodic(const Duration(seconds: 1), (_) => _tick());
    state = TimerState(
      total: state.total,
      remaining: state.remaining,
      running: true,
      isBreak: state.isBreak,
    );
  }

  void reset() {
    _ticker?.cancel();
    _saveStudyTime();
    state = const TimerState(
      total: Duration.zero,
      remaining: Duration.zero,
      running: false,
      isBreak: false,
    );
  }

  @override
  void dispose() {
    _ticker?.cancel();
    _saveStudyTime();
    super.dispose();
  }
}

