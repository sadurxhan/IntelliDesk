/// TASKS (TO-DO) PROVIDER (FIXED FOR RTDB)
/// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
class TasksController extends StateNotifier<List<Task>> {
  final String userId;
  late final CollectionReference<Map<String, dynamic>> _tasksRef;
  StreamSubscription<QuerySnapshot<Map<String, dynamic>>>? _sub;

  TasksController(this.userId) : super(const []) {
    if (userId.isEmpty) return;
    _tasksRef = _fs.collection('users').doc(userId).collection('tasks');
    _listen();
  }

  void _listen() {
    _sub?.cancel();
    _sub = _tasksRef
        .orderBy('done')
        .orderBy('createdAt', descending: true)
        .snapshots()
        .listen(
      (snapshot) {
        try {
          final tasks =
              snapshot.docs.map((doc) => Task.fromFirestore(doc)).toList();
          state = tasks;
        } catch (e, st) {
          debugPrint('âŒ Firestore Task parsing error: $e\n$st');
          state = [];
        }
      },
      onError: (error) {
        debugPrint('âŒ Firestore Task stream error: $error');
        state = [];
      },
    );
  }

  Future<void> add(String title, {DateTime? due}) async {
    if (title.trim().isEmpty || userId.isEmpty) return;
    try {
      await _tasksRef.add({
        'title': title.trim(),
        'due': due != null ? Timestamp.fromDate(due) : null, // âœ… FIXED
        'done': false,
        'createdAt': FieldValue.serverTimestamp(),
      });
      debugPrint('âœ… Task added: $title');
    } catch (e, st) {
      debugPrint('âŒ Failed to add task: $e\n$st');
    }
  }

  Future<void> toggle(String id) async {
    if (userId.isEmpty || id.isEmpty) return;
    try {
      final task = state.firstWhere((t) => t.id == id);
      await _tasksRef.doc(id).update({'done': !task.done});
    } catch (e, st) {
      debugPrint('âŒ Failed to toggle task: $e\n$st');
    }
  }

  Future<void> remove(String id) async {
    if (userId.isEmpty || id.isEmpty) return;
    try {
      await _tasksRef.doc(id).delete();
      debugPrint('ğŸ—‘ï¸ Task removed: $id');
    } catch (e, st) {
      debugPrint('âŒ Failed to remove task: $e\n$st');
    }
  }

  @override
  void dispose() {
    _sub?.cancel();
    super.dispose();
  }
}

