class _AuthFormBody extends StatefulWidget {
  final bool isLogin;
  final Future<void> Function(String email, String password) onSubmit;

  const _AuthFormBody({required this.isLogin, required this.onSubmit});

  @override
  State<_AuthFormBody> createState() => _AuthFormBodyState();
}

class _AuthFormBodyState extends State<_AuthFormBody> {
  final _emailC = TextEditingController();
  final _passC = TextEditingController();
  final _confirmPassC = TextEditingController();

  bool _obscurePass = true;
  bool _obscureConfirm = true;
  bool _busy = false;
  String? _errorText;

  @override
  void dispose() {
    _emailC.dispose();
    _passC.dispose();
    _confirmPassC.dispose();
    super.dispose();
  }

  Future<void> _submit() async {
    if (!mounted) return;
    setState(() {
      _busy = true;
      _errorText = null;
    });

    if (!widget.isLogin && _passC.text != _confirmPassC.text) {
      setState(() {
        _errorText = "Passwords do not match";
        _busy = false;
      });
      return;
    }

    try {
      await widget.onSubmit(_emailC.text.trim(), _passC.text);
      // Auth listener will handle navigation
    } on FirebaseAuthException catch (e) {
      if (!mounted) return;
      setState(() {
        _errorText = authErrorMessage(e);
        _busy = false;
      });
    } catch (e) {
      if (!mounted) return;
      setState(() {
        _errorText = "An unknown error occurred.";
        _busy = false;
      });
    }
  }

  @override
  Widget build(BuildContext context) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        // Email
        const Text("Email", style: TextStyle(fontWeight: FontWeight.w500)),
        const SizedBox(height: 8),
        TextField(
          controller: _emailC,
          keyboardType: TextInputType.emailAddress,
          decoration: const InputDecoration(
            hintText: 'Enter your email',
            contentPadding: EdgeInsets.all(15),
          ),
        ),
        const SizedBox(height: 16),
        // Password
        const Text("Password", style: TextStyle(fontWeight: FontWeight.w500)),
        const SizedBox(height: 8),
        TextField(
          controller: _passC,
          obscureText: _obscurePass,
          decoration: InputDecoration(
            hintText: 'Enter your password',
            contentPadding: const EdgeInsets.all(15),
            suffixIcon: IconButton(
              icon: Icon(
                _obscurePass ? Icons.visibility_off : Icons.visibility,
              ),
              onPressed: () => setState(() => _obscurePass = !_obscurePass),
            ),
          ),
        ),
        if (!widget.isLogin) ...[
          const SizedBox(height: 16),
          // Confirm Password
          const Text(
            "Confirm Password",
            style: TextStyle(fontWeight: FontWeight.w500),
          ),
          const SizedBox(height: 8),
          TextField(
            controller: _confirmPassC,
            obscureText: _obscureConfirm,
            decoration: InputDecoration(
              hintText: 'Confirm your password',
              contentPadding: const EdgeInsets.all(15),
              suffixIcon: IconButton(
                icon: Icon(
                  _obscureConfirm ? Icons.visibility_off : Icons.visibility,
                ),
                onPressed: () =>
                    setState(() => _obscureConfirm = !_obscureConfirm),
              ),
            ),
          ),
        ],
        const SizedBox(height: 24),
        if (_errorText != null)
          Padding(
            padding: const EdgeInsets.only(bottom: 16.0),
            child: Text(
              _errorText!,
              style: const TextStyle(
                color: Colors.red,
                fontWeight: FontWeight.w500,
              ),
            ),
          ),
        FilledButton(
          onPressed: _busy ? null : _submit,
          child: _busy
              ? const SizedBox(
                  height: 24,
                  width: 24,
                  child: CircularProgressIndicator(
                    color: Colors.white,
                    strokeWidth: 3,
                  ),
                )
              : Text(widget.isLogin ? 'Login' : 'Create Account'),
        ),
      ],
    );
  }
}

